    tc.value = "Resource Requirements"
    tc.font  = Font(color=WHITE, bold=True, size=14)
    tc.fill  = PatternFill("solid", fgColor=DARK_BLUE)
    tc.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 28

    headers = ["Task ID", "Task", "Owner", "Time Requirements", "Resource Requirements", "Status", "Notes"]
    header_style(ws, 2, len(headers), bg="375623")
    for c, h in enumerate(headers, 1):
        ws.cell(row=2, column=c).value = h
    ws.row_dimensions[2].height = 22

    for r_idx, row in enumerate(rows, 3):
        values = [
            clean(row.get("Task ID", "")),
            clean(row.get("Task", "")),
            get_owner(row),
            clean(row.get("Time requirements", "—")),
            clean(row.get("Resource requirements", "")),
            clean(row.get("Status", "")),
            clean(row.get("Assumption / unknown notes", "")),
        ]
        for c, val in enumerate(values, 1):
            cell = ws.cell(row=r_idx, column=c, value=val)
            cell.border = thin_border()
            cell.alignment = Alignment(vertical="center", wrap_text=(c >= 4))
            if r_idx % 2 == 0:
                cell.fill = PatternFill("solid", fgColor="E2EFDA")

        sf = status_fill(values[5])   # Status is now index 5 (shifted by Time requirements)
        if sf:
            for c in range(1, len(headers) + 1):
                ws.cell(row=r_idx, column=c).fill = sf

        ws.row_dimensions[r_idx].height = 36

    set_col_widths(ws, [10, 28, 18, 28, 38, 14, 40])
    ws.freeze_panes = "A3"


