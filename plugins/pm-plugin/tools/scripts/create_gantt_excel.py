# Parse the "Project management source table" from gantt_chart.md
# ---------------------------------------------------------------------------

def parse_source_table(md_path: Path) -> list[dict]:
    text = md_path.read_text(encoding="utf-8")

    # Find the section
    match = re.search(
        r"## Project management source table\s*\n(.*?)(?=\n## |\Z)",
        text, re.DOTALL
    )
    if not match:
        sys.exit("ERROR: Could not find '## Project management source table' in gantt_chart.md")

    section = match.group(1)
    lines = [l.strip() for l in section.strip().splitlines() if l.strip().startswith("|")]

    if len(lines) < 3:
        sys.exit("ERROR: Source table has fewer than 3 rows (need header + separator + data).")

    # Parse header
    header = [h.strip() for h in lines[0].strip("|").split("|")]
    rows = []
    for line in lines[2:]:          # skip separator row
        cells = [c.strip() for c in line.strip("|").split("|")]
        # pad or truncate to match header length
        while len(cells) < len(header):
            cells.append("")
        rows.append(dict(zip(header, cells)))

    return rows


def clean(val: str) -> str:
    return val.strip().strip("**").strip()


def get_owner(row: dict) -> str:
    """Return owner field, handling both 'Owner' (template v1) and legacy 'Owner / role'."""
    return clean(row.get("Owner") or row.get("Owner / role", ""))


def to_date(val: str) -> date | None:
    val = clean(val)
    try:
        return date.fromisoformat(val)
    except ValueError:
        return None


def to_int(val: str) -> int:
    try:
        return int(clean(val).replace("%", ""))
    except ValueError:
        return 0


# ---------------------------------------------------------------------------
# Helpers: styling
# ---------------------------------------------------------------------------

def header_style(ws, row: int, cols: int,
                 bg=DARK_BLUE, fg=WHITE, bold=True, size=11):
    fill = PatternFill("solid", fgColor=bg)
    font = Font(color=fg, bold=bold, size=size)
    align = Alignment(horizontal="center", vertical="center", wrap_text=True)
    for c in range(1, cols + 1):
        cell = ws.cell(row=row, column=c)
        cell.fill = fill
        cell.font = font
        cell.alignment = align


def thin_border():
    thin = Side(style="thin", color="CCCCCC")
    return Border(left=thin, right=thin, top=thin, bottom=thin)


def apply_borders(ws, min_row, max_row, min_col, max_col):
    bd = thin_border()
    for r in range(min_row, max_row + 1):
        for c in range(min_col, max_col + 1):
            ws.cell(row=r, column=c).border = bd


def set_col_widths(ws, widths: list[int]):
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


def status_fill(status: str) -> PatternFill | None:
    s = status.lower()
    if "at risk" in s:
        return PatternFill("solid", fgColor="FFE699")
    if "not started" in s:
        return PatternFill("solid", fgColor=LIGHT_GREY)
    if "in progress" in s:
        return PatternFill("solid", fgColor="DDEBF7")
    if "complete" in s:
        return PatternFill("solid", fgColor="E2EFDA")
    return None


