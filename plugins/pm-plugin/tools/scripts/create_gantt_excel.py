def sheet_gantt_timeline(wb, rows: list[dict]):
    ws = wb.create_sheet("Gantt Timeline")

    tasks = []
    for row in rows:
        start = to_date(clean(row.get("Estimated start", "")))
        end   = to_date(clean(row.get("Estimated end", "")))
        if start and end:
            tasks.append({
                "id":    clean(row.get("Task ID", "")),
                "task":  clean(row.get("Task", "")),
                "owner": get_owner(row),
                "start": start,
                "end":   end,
                "dur":   clean(row.get("Duration (business days)", "")),
                "ms":    clean(row.get("Milestone?", "")).lower() == "yes",
                "prog":  to_int(clean(row.get("Progress", "0"))),
                "status":clean(row.get("Status", "")),
            })

    if not tasks:
        ws["A1"] = "No tasks with valid start/end dates found."
        return

    proj_start = min(t["start"] for t in tasks)
    proj_end   = max(t["end"]   for t in tasks)

    # Generate week-start dates (Mondays)
    week_starts = []
    d = proj_start - timedelta(days=proj_start.weekday())  # prev Monday
    while d <= proj_end + timedelta(days=7):
        week_starts.append(d)
        d += timedelta(days=7)

    FIXED_COLS = 6   # ID | Task | Owner | Start | End | Duration
    DATA_COL_START = FIXED_COLS + 1

    # Title
    total_cols = FIXED_COLS + len(week_starts)
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=total_cols)
    tc = ws.cell(row=1, column=1, value="Gantt Timeline")
    tc.font  = Font(color=WHITE, bold=True, size=14)
    tc.fill  = PatternFill("solid", fgColor=DARK_BLUE)
    tc.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 28

    # Fixed headers
    fixed_headers = ["Task ID", "Task", "Owner", "Start", "End", "Duration (d)"]
    for c, h in enumerate(fixed_headers, 1):
        cell = ws.cell(row=2, column=c, value=h)
        cell.fill = PatternFill("solid", fgColor=MID_BLUE)
        cell.font = Font(color=WHITE, bold=True)
        cell.alignment = Alignment(horizontal="center", vertical="center")
        cell.border = thin_border()
    ws.row_dimensions[2].height = 22

    # Week headers
    today = date.today()
    for i, wk in enumerate(week_starts):
        c = DATA_COL_START + i
        cell = ws.cell(row=2, column=c)
        cell.value = wk
        cell.number_format = "MMM D"
        if wk <= today <= wk + timedelta(days=6):
            cell.fill = PatternFill("solid", fgColor=AMBER)
            cell.font = Font(color="000000", bold=True, size=8)
        else:
            cell.fill = PatternFill("solid", fgColor=MID_BLUE)
            cell.font = Font(color=WHITE, bold=True, size=8)
        cell.alignment = Alignment(horizontal="center", vertical="center", text_rotation=90)
        cell.border = thin_border()
        ws.column_dimensions[get_column_letter(c)].width = 5

    # Data rows
    for r_idx, task in enumerate(tasks, 3):
        ws.cell(row=r_idx, column=1, value=task["id"])
        ws.cell(row=r_idx, column=2, value=task["task"])
        ws.cell(row=r_idx, column=3, value=task["owner"])

        sc = ws.cell(row=r_idx, column=4, value=task["start"])
        sc.number_format = "YYYY-MM-DD"
        ec = ws.cell(row=r_idx, column=5, value=task["end"])
        ec.number_format = "YYYY-MM-DD"
        ws.cell(row=r_idx, column=6, value=task["dur"])

        for col_idx in range(1, FIXED_COLS + 1):
            cell = ws.cell(row=r_idx, column=col_idx)
            cell.alignment = Alignment(vertical="center")
            cell.border = thin_border()
            if r_idx % 2 == 0:
                cell.fill = PatternFill("solid", fgColor=PALE_BLUE)

        # Draw task bars
        bar_colour  = MILESTONE_C if task["ms"] else MID_BLUE
        bar_fill    = PatternFill("solid", fgColor=bar_colour)
        empty_fill  = PatternFill("solid", fgColor=LIGHT_GREY if r_idx % 2 == 0 else WHITE)

        for i, wk in enumerate(week_starts):
            c = DATA_COL_START + i
            cell = ws.cell(row=r_idx, column=c)
            cell.border = thin_border()
            wk_end = wk + timedelta(days=6)
            if task["start"] <= wk_end and task["end"] >= wk:
                cell.fill = bar_fill
                if task["ms"]:
                    cell.value = "◆"
                    cell.font  = Font(color=WHITE, size=7, bold=True)
                    cell.alignment = Alignment(horizontal="center", vertical="center")
            else:
                cell.fill = empty_fill

        ws.row_dimensions[r_idx].height = 18

    # Column widths for fixed columns
    for c, w in zip(range(1, FIXED_COLS + 1), [10, 28, 16, 13, 13, 8]):
        ws.column_dimensions[get_column_letter(c)].width = w

    ws.freeze_panes = f"{get_column_letter(DATA_COL_START)}3"


