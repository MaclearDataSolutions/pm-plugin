def sheet_project_view(wb, rows: list[dict]):
    ws = wb.create_sheet("Project View")

    columns = [
        "Task ID", "Work Package", "Task", "Task Description",
        "Owner", "Time Requirements", "Resource Requirements",
        "Est. Start", "Est. End", "Duration (days)",
        "Milestone?", "Milestone Date", "Depends On",
        "Progress", "Status", "Notes"
    ]
    col_map = {
        "Task ID":               lambda r: clean(r.get("Task ID", "")),
        "Work Package":          lambda r: clean(r.get("Work package", "")),
        "Task":                  lambda r: clean(r.get("Task", "")),
        "Task Description":      lambda r: clean(r.get("Task description", "")),
        "Owner":                 lambda r: get_owner(r),
        "Time Requirements":     lambda r: clean(r.get("Time requirements", "—")),
        "Resource Requirements": lambda r: clean(r.get("Resource requirements", "")),
        "Est. Start":            lambda r: clean(r.get("Estimated start", "")),
        "Est. End":              lambda r: clean(r.get("Estimated end", "")),
        "Duration (days)":       lambda r: clean(r.get("Duration (business days)", "")),
        "Milestone?":            lambda r: clean(r.get("Milestone?", "")),
        "Milestone Date":        lambda r: clean(r.get("Milestone date", "")),
        "Depends On":            lambda r: clean(r.get("Depends on", "")),
        "Progress":              lambda r: clean(r.get("Progress", "")),
        "Status":                lambda r: clean(r.get("Status", "")),
        "Notes":                 lambda r: clean(r.get("Assumption / unknown notes", "")),
    }

    # Title
    ncols = len(columns)
    ws.merge_cells(f"A1:{get_column_letter(ncols)}1")
    title = ws["A1"]
    title.value = "Project View"
    title.font = Font(color=WHITE, bold=True, size=14)
    title.fill = PatternFill("solid", fgColor=DARK_BLUE)
    title.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 28

    header_style(ws, 2, ncols, bg=MID_BLUE)
    for c, col in enumerate(columns, 1):
        ws.cell(row=2, column=c).value = col
    ws.row_dimensions[2].height = 30

    for r_idx, row in enumerate(rows, 3):
        for c_idx, col in enumerate(columns, 1):
            val = col_map[col](row)
            cell = ws.cell(row=r_idx, column=c_idx)

            # Date columns
            if col in ("Est. Start", "Est. End", "Milestone Date"):
                d = to_date(val)
                cell.value = d if d else val
                if d:
                    cell.number_format = "YYYY-MM-DD"
            elif col == "Progress":
                pct = to_int(val)
                cell.value = pct / 100
                cell.number_format = "0%"
            elif col == "Duration (days)":
                try:
                    cell.value = int(val)
                except ValueError:
                    cell.value = val
            else:
                cell.value = val

            cell.alignment = Alignment(vertical="center", wrap_text=(c_idx >= 4))
            cell.border = thin_border()

            # Row shading
            bg = status_fill(row.get("Status", ""))
            if bg:
                cell.fill = bg
            elif r_idx % 2 == 0:
                cell.fill = PatternFill("solid", fgColor=PALE_BLUE)

            # Milestone rows — purple tint (overrides status colour)
            if clean(row.get("Milestone?", "")).lower() == "yes":
                cell.fill = PatternFill("solid", fgColor="EAD1DC")

        ws.row_dimensions[r_idx].height = 40

    set_col_widths(ws, [8, 22, 26, 42, 18, 22, 32, 14, 14, 10, 10, 14, 14, 10, 14, 40])
    ws.freeze_panes = "A3"
    ws.auto_filter.ref = f"A2:{get_column_letter(ncols)}2"


