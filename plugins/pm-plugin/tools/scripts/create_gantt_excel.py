def sheet_milestones(wb, rows: list[dict]):
    ws = wb.create_sheet("Milestones")

    ms_rows = [r for r in rows if clean(r.get("Milestone?", "")).lower() == "yes"]

    ws.merge_cells("A1:G1")
    tc = ws["A1"]
    tc.value = "Milestones"
    tc.font  = Font(color=WHITE, bold=True, size=14)
    tc.fill  = PatternFill("solid", fgColor=DARK_BLUE)
    tc.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 28

    headers = ["Task ID", "Milestone / Task", "Milestone Date", "Owner", "Depends On", "Status", "Notes"]
    header_style(ws, 2, len(headers), bg=MILESTONE_C)
    for c, h in enumerate(headers, 1):
        ws.cell(row=2, column=c).value = h
    ws.row_dimensions[2].height = 22

    for r_idx, row in enumerate(ms_rows, 3):
        values = [
            clean(row.get("Task ID", "")),
            clean(row.get("Task", "")),
            to_date(clean(row.get("Milestone date", ""))) or clean(row.get("Milestone date", "")),
            get_owner(row),
            clean(row.get("Depends on", "")),
            clean(row.get("Status", "")),
            clean(row.get("Assumption / unknown notes", "")),
        ]
        for c, val in enumerate(values, 1):
            cell = ws.cell(row=r_idx, column=c, value=val)
            if c == 3 and isinstance(val, date):
                cell.number_format = "YYYY-MM-DD"
            cell.border = thin_border()
            cell.alignment = Alignment(vertical="center", wrap_text=(c >= 6))
            if r_idx % 2 == 0:
                cell.fill = PatternFill("solid", fgColor="F3E6F9")
        ws.row_dimensions[r_idx].height = 30

    apply_borders(ws, 2, 2 + len(ms_rows), 1, len(headers))
    set_col_widths(ws, [10, 32, 16, 18, 16, 14, 45])
    ws.freeze_panes = "A3"


