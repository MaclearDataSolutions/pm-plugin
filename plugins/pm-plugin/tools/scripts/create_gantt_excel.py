# Sheet 5 — Assumptions & Unknowns
# ---------------------------------------------------------------------------

def sheet_assumptions(wb, rows: list[dict]):
    ws = wb.create_sheet("Assumptions Unknowns")

    ws.merge_cells("A1:D1")
    tc = ws["A1"]
    tc.value = "Assumptions and Unknowns"
    tc.font  = Font(color=WHITE, bold=True, size=14)
    tc.fill  = PatternFill("solid", fgColor=DARK_BLUE)
    tc.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 28

    headers = ["Task ID", "Task", "Type", "Assumption / Unknown"]
    header_style(ws, 2, len(headers), bg="843C0C")
    for c, h in enumerate(headers, 1):
        ws.cell(row=2, column=c).value = h
    ws.row_dimensions[2].height = 22

    r_idx = 3
    amber_fill  = PatternFill("solid", fgColor="FFF2CC")
    red_fill    = PatternFill("solid", fgColor="FFE0E0")
    normal_fill = PatternFill("solid", fgColor=LIGHT_GREY)

    for row in rows:
        note = clean(row.get("Assumption / unknown notes", ""))
        if not note or note.lower() in ("", "—", "-"):
            continue
        tl = clean(row.get("Timeline basis", ""))
        note_type = "Assumption" if "planning assumption" in note.lower() or "estimate" in note.lower() or "assumption" in tl.lower() else "Unknown / confirm"

        values = [
            clean(row.get("Task ID", "")),
            clean(row.get("Task", "")),
            note_type,
            note,
        ]
        for c, val in enumerate(values, 1):
            cell = ws.cell(row=r_idx, column=c, value=val)
            cell.border = thin_border()
            cell.alignment = Alignment(vertical="center", wrap_text=True)
            if "unknown" in note_type.lower():
                cell.fill = red_fill
            elif "assumption" in note_type.lower():
                cell.fill = amber_fill
            else:
                cell.fill = normal_fill
        ws.row_dimensions[r_idx].height = 40
        r_idx += 1

    set_col_widths(ws, [10, 28, 18, 60])
    ws.freeze_panes = "A3"


# ---------------------------------------------------------------------------
# CSV export
# ---------------------------------------------------------------------------

def export_csv(rows: list[dict], csv_path: Path):
    if not rows:
        return
    fieldnames = list(rows[0].keys())
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)
    print(f"  CSV  -> {csv_path}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    if not GANTT_MD.exists():
        sys.exit(f"ERROR: {GANTT_MD} not found. Create gantt_chart.md first.\n"
                 f"  Tip: pass --gantt <path> to target a different folder.")

    print(f"Reading {GANTT_MD} ...")
    rows = parse_source_table(GANTT_MD)
    print(f"  Found {len(rows)} rows in source table.")

    wb = openpyxl.Workbook()
    wb.remove(wb.active)   # remove default sheet

    print("Building sheets ...")
    sheet_project_view(wb, rows)
    sheet_gantt_timeline(wb, rows)
    sheet_milestones(wb, rows)
    sheet_resources(wb, rows)
    sheet_assumptions(wb, rows)

    wb.save(EXCEL_OUT)
    print(f"  XLSX -> {EXCEL_OUT}")

    export_csv(rows, CSV_OUT)
    print("Done.")


if __name__ == "__main__":
    main()
