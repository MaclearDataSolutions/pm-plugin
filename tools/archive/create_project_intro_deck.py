def simple_table(slide, headers, rows_data, left, top, width, row_height=Inches(0.42)):
    col_count = len(headers)
    col_w = width / col_count

    # Header row
    for c, h in enumerate(headers):
        x = left + c * col_w
        add_rect(slide, x, top, col_w, row_height, BRAND_NAVY)
        add_text_box(slide, h, x + Inches(0.05), top + Inches(0.05), col_w - Inches(0.1), row_height - Inches(0.05),
                     font_name=BODY_FONT, font_size=Pt(11), bold=True, color=WHITE)

    # Data rows
    for r_idx, row in enumerate(rows_data):
        y = top + (r_idx + 1) * row_height
        bg = LIGHT_GREY if r_idx % 2 == 0 else WHITE
        for c_idx, cell in enumerate(row):
            x = left + c_idx * col_w
            add_rect(slide, x, y, col_w, row_height, bg)
            cell_color = BLACK
            if "At risk" in str(cell):
                cell_color = RGBColor(0xC0, 0x00, 0x00)
                bg_fill = RGBColor(0xFF, 0xE6, 0x99)
                add_rect(slide, x, y, col_w, row_height, bg_fill)
            elif "TBD" in str(cell) or "Not provided" in str(cell):
                cell_color = RGBColor(0x70, 0x50, 0x00)
            add_text_box(slide, str(cell),
                         x + Inches(0.05), y + Inches(0.04),
                         col_w - Inches(0.1), row_height - Inches(0.05),
                         font_name=BODY_FONT, font_size=Pt(10), color=cell_color)


